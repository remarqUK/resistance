def get_correlated_pairs(pair: str) -> set:
    """Return set of pairs correlated with the given pair."""
    correlated = set()
    for group, members in CORRELATION_GROUPS.items():
        if pair in members:
            correlated.update(members)
    correlated.discard(pair)
    return correlated


@dataclass
class Signal:
    """A trading signal generated by the strategy."""
    time: pd.Timestamp
    pair: str
    direction: str      # 'LONG' or 'SHORT'
    entry_price: float
    sl_price: float
    tp_price: float
    zone_upper: float
    zone_lower: float
    zone_strength: str
    zone_type: str
    quality_score: float = 0.0


@dataclass
class Trade:
    """A completed or open trade."""
    entry_time: pd.Timestamp
    entry_price: float
    direction: str
    sl_price: float
    tp_price: float
    zone_upper: float
    zone_lower: float
    zone_strength: str
    risk: float             # dollar distance of full risk (entry to SL)
    exit_time: Optional[pd.Timestamp] = None
    exit_price: Optional[float] = None
    exit_reason: Optional[str] = None   # 'TP', 'SL', 'EARLY_EXIT', 'SIDEWAYS', 'TIME', 'FRIDAY', 'END'
