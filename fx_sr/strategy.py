"""Trading strategy: mean reversion at daily S/R zones.

Refined strategy rules (from all 30 NickShawnFX videos):
1. Identify zones on WEEKLY/DAILY chart
2. Enter on 1-HOUR chart when price is INSIDE the zone and a reversal candle appears
3. Price must be at least HALFWAY into the zone before entry
4. Configured risk-to-reward ratio (default 1.2R target)
5. Stop loss beyond zone + all recent wicks + breathing room
6. CUT LOSSES EARLY: zone break, sideways action, time limit, structure break
7. HOLD WINNERS: don't close early (exception: Friday close near TP)
8. CORRELATION: cap simultaneous correlated exposure
9. MOMENTUM FILTER: skip entries with strong momentum into zone
"""

